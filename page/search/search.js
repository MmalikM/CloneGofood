Page({
  data: {
    value:"",
    hot:[],
    history:[],
    resultSearch:[]
  },
  onLoad() {
    this.setData({
      hot: getApp().globalData.keyword,
      history:getApp().globalData.riwayatPencarian
    })
  },
  onInput(word){
    this.setData({
      value : word
    })
    
    let resultSearch = makanan.filter(function(item) {
      return item.name.toLowerCase().includes(word.toLowerCase());
    });
    this.setData({resultSearch:resultSearch})
    if(word.length===0){
      this.setData({resultSearch:[]})
    } 
  },
  onClear() {
    this.setData({
      value: '',
      resultSearch:[]
    });
  },
  onCancel() {
    this.setData({
      resultSearch:[],
      value: '',
    });
    my.navigateBack();
  },
  onListItemSearch(e){
    const {id}= e.target.dataset;

    // ----- masukin keyword ----- 
    if(getApp().globalData.keyword.length > 0){
      getApp().globalData.keyword.push({name:this.data.value})
    }
    // if(getApp().globalData.keyword.length > 10){
    //   getApp().globalData.keyword.slice(1); 
    // }
    if(getApp().globalData.keyword.length === 0) getApp().globalData.keyword =[{name:this.data.value}]
  
    // ----- masukin riwayat ----

    const detailItem = makanan.find(el=>el.id === id)
    if(getApp().globalData.riwayatPencarian.length > 0){
      getApp().globalData.riwayatPencarian.push(detailItem)
    }
    if(getApp().globalData.riwayatPencarian.length === 0) getApp().globalData.riwayatPencarian =[detailItem]
    
    my.redirectTo({
      url:"/page/detail-item/detail-item?id="+id
    })
  },
});
